    public void readJTable1() {
        DefaultTableModel tabelaProdutos = (DefaultTableModel) jTable1.getModel();
        tabelaProdutos.setNumRows(0);
        model.bean.DaoImplBebidas bDao = new DaoImplBebidas();
        model.bean.DaoImplAssados aDao = new DaoImplAssados();
        model.bean.DaoImplAdicional adDao = new DaoImplAdicional();

        for (Adicionais adicional : adDao.readAdicionais()) {
            tabelaProdutos.addRow(new Object[]{
                adicional.getQuantidadeAdicionais(),
                adicional.getNomeAdicionais(),
                adicional.getValorAdicionais()
            });
        }

        for (Bebidas bebida : bDao.readBebidas()) {
            tabelaProdutos.addRow(new Object[]{
                bebida.getQuantidade(),
                bebida.getNome(),
                bebida.getValor()
            });
        }

        for (Assados assado : aDao.readAssados()) {
            tabelaProdutos.addRow(new Object[]{
                assado.getQuantidade(),
                assado.getNome(),
                assado.getValor()
            });
        }
    }

    public void readJTable1ForLike(String descricao) {
        DefaultTableModel tabelaProdutos = (DefaultTableModel) jTable1.getModel();
        tabelaProdutos.setNumRows(0);
        model.bean.DaoImplBebidas bDao = new DaoImplBebidas();
        model.bean.DaoImplAssados aDao = new DaoImplAssados();
        model.bean.DaoImplAdicional adDao = new DaoImplAdicional();

        for (Bebidas bebida : bDao.buscaForLikeBebida(descricao)) {
            tabelaProdutos.addRow(new Object[]{
                bebida.getQuantidade(),
                bebida.getNome(),
                bebida.getValor()
            });
        }

        for (Adicionais adicional : adDao.buscaForLikeAdicional(descricao)) {
            tabelaProdutos.addRow(new Object[]{
                adicional.getQuantidadeAdicionais(),
                adicional.getNomeAdicionais(),
                adicional.getValorAdicionais()
            });
        }

        for (Assados assado : aDao.buscaForLikeAssados(descricao)) {
            tabelaProdutos.addRow(new Object[]{
                assado.getQuantidade(),
                assado.getNome(),
                assado.getValor()
            });
        }
    }

    private boolean validaSalvarContato() {
        String msg = "";
        Boolean verificar = true;
        if (jTextField1.getText().equals("")) {
            msg += "* nome é obrigatório \n";
            verificar = false;
        }
        if (cbTipo.getSelectedItem().toString().equals("Selecione um tipo")) {
            msg += "* tipo é obrigatório \n";
            verificar = false;
        }
        if (jTextField12.getText().equals("")) {
            msg += "* valor é obrigatório \n";
            verificar = false;
        }
        if (!verificar) {
            JOptionPane.showMessageDialog(null, msg);
        }
        return verificar;
    }

