    private void jButtonREMOVERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonREMOVERActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            String menu;
            menu = JOptionPane.showInputDialog("Digite a opção desejada \n1- Assado \n2- Bebida \n3- Adicional \nS- Sair");

            switch (menu) {//0-Selecione um tipo 1-Assado 2-Bebida 3-Adicional
                case "1":
                    model.bean.DaoImplAssados aDao = new DaoImplAssados();
                    Assados a = new Assados();
                    a.setIdAssados((int) jTable1.getValueAt(jTable1.getSelectedRow(), 4));
                    aDao.deleteAssados(a);
                    break;
                case "2":
                    model.bean.DaoImplBebidas bDao = new DaoImplBebidas();
                    Bebidas b = new Bebidas();
                    b.setIdBebidas((int) jTable1.getValueAt(jTable1.getSelectedRow(), 4));
                    bDao.deleteBebidas(b);
                    break;
                case "3":
                    model.bean.DaoImplAdicional adDao = new DaoImplAdicional();
                    Adicionais ad = new Adicionais();
                    ad.setIdAdicionais((int) jTable1.getValueAt(jTable1.getSelectedRow(), 4));
                    adDao.deleteAdicionais(ad);
                    break;
            }

            //LIMPAR CAMPOS
            cbTipo.setSelectedItem("Selecione um tipo");
            jTextField1.setText(null);
            jTextField12.setText(null);
            jTextField2.setText(null);
            jTextField22.setText(null);
            jTextField3.setText(null);
            jTextField32.setText(null);
            jTextField4.setText(null);
            jTextField42.setText(null);
            jTextField5.setText(null);
            jTextField52.setText(null);
            QTD1.setText(null);
            QTD2.setText(null);
            QTD3.setText(null);
            QTD4.setText(null);
            QTD5.setText(null);

            //MOSTRANDO VALORES NA JTABLE
            readJTable1();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir");
        }
    }//GEN-LAST:event_jButtonREMOVERActionPerformed

